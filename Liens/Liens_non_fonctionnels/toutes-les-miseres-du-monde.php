    <p class="epigraph textModule textModule--type-epigraph ">Bref, la famille encaisse les coups du début à la fin, sans qu’une lueur d’espoir trouve son chemin entre les murs nus de sa froide demeure, où il n’y a que du gruau pour se sustenter. </p>

                    
    <p class="paragraph textModule textModule--type-paragraph ">Certes, la pauvreté est encore (et peut-être plus que jamais) d’actualité. La violence familiale (qu’elle soit verbale ou sexuelle) n’a pas été reléguée aux affres du passé. Mais la dramaturgie d’ici et d’ailleurs ne manque pas d’exemples pour nous le rappeler avec plus de subtilités et de nuances. </p>

                    
    <p class="paragraph textModule textModule--type-paragraph ">Le jeu des acteurs – encore là en manque cruel de nuances – ajoute à ce sentiment d’assister à une pièce à la dérive. Le père (Frédéric Boivin) n’est que cris. Les filles semblent d’une pauvreté mentale à la limite de la déficience, riant aux moments les moins opportuns. Même Nathalie Mallette, une actrice capable de nous émouvoir avec un seul regard, n’arrive pas à déclencher en nous la moindre sympathie dans son rôle de mère stoïque, incapable d’un seul geste pour protéger sa famille. </p>

                    
    <p class="paragraph textModule textModule--type-paragraph ">La pièce « dépeint un pan de notre histoire qu’il ne faut pas occulter », écrit la directrice artistique du Rideau Vert, Denise Filiatrault, dans le programme du spectacle. Certes, il ne faut pas oublier les peines et les misères de ceux et celles qui nous ont précédés, comme il faut célébrer la langue crue et sans compromis qui est celle de Jeanne-Mance Delisle. </p>

                    
    <p class="paragraph textModule textModule--type-paragraph ">Cela étant dit, on a eu toutes les misères du monde à se passionner pour ce reel théâtral qui, au bout du compte, n’est ni beau ni poignant… </p>

