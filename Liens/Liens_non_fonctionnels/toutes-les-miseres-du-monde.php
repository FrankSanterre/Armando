    <p class="paragraph textModule textModule--type-paragraph ">La pièce créée en 1978 (et présentée pour la dernière fois au TNM en 1983) a marqué la dramaturgie d’ici avec sa langue dure et acérée. Le spectacle présenté ces jours-ci risque toutefois de moins marquer les esprits. </p>

                    
    <p class="paragraph textModule textModule--type-paragraph ">Faut-il tenir responsable le passage du temps, qui érode la pertinence de certains chefs-d’œuvre de jadis ? Est-ce que l’approche tonne de briques de Marc Béland à la mise en scène y est pour quelque chose ? </p>

                    
    <p class="epigraph textModule textModule--type-epigraph ">Toujours est-il que ce spectacle tracé à gros traits n’a pas réussi à nous convaincre de la nécessité de présenter de nouveau cette pièce qualifiée de « diamant noir de la dramaturgie québécoise ».</p>

                    
    <p class="paragraph textModule textModule--type-paragraph ">De noirceur, il est certes question ici. La pièce raconte l’enfer d’une famille vivant dans la plus grande pauvreté, au fond d’un rang de l’Abitibi, dans les années 1950. Venus chercher fortune dans cette supposée terre promise, les Morin n’ont trouvé qu’une terre ingrate où se poser. Le père, Tonio, règne en despote sur son clan. Il n’est que cruauté et perversion pour sa femme Laurette et ses trois filles. Seul son fils Gérald, que tous surnomment Ti-fou, semble trouver grâce à ses yeux. </p>

