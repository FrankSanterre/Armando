                            setUpPanorama();
                        }else if (LPAds.isNativeAdSerieXtra(slotData.creativeTemplateId)) {
                            setUpNativeAdSerieXtra();
                        }
                    }
                });

                LPAds.displayAdSpot(slotName);
            })()
