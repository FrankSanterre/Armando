    <p class="paragraph textModule textModule--type-paragraph hasFeaturedAuthor">Et ce n’est pas en sortant un lapin d’un chapeau (c’est-à-dire en ramenant le troisième lien) qu’on rétablira automatiquement la confiance des électeurs. On risque plutôt d’alimenter la confusion quant aux réelles intentions du gouvernement. En d’autres mots, le mal est fait. </p>

