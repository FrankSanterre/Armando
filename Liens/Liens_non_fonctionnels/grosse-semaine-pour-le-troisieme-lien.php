    <p class="paragraph textModule textModule--type-paragraph hasFeaturedAuthor">M. Legault aurait-il dû s’en tenir à sa première réaction ? Le soir de la défaite, son jugement semblait plus sûr. Quand il a déclaré : « Marie-Anik, tu n’as pas perdu. C’est la CAQ et moi qui avons perdu », il donnait l’impression d’avoir entendu le message que lui envoyait l’électorat. Par quelle contorsion intellectuelle en est-il arrivé à conclure qu’il fallait ressusciter le projet de troisième lien dès le lendemain matin ? Mystère… </p>

                    <div class="quote">
    <div>
                    <p class="quoteText">Lors de son discours [le soir de la défaite], j’ai trouvé qu’il était très humble. Mais on n’aboutit pas à une conclusion en 12 heures. Il aurait dû prendre le temps de réfléchir. Le mot qui me vient à l’esprit est “incohérence”, et pas seulement cette semaine.</p>
