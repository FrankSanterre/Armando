    <p class="paragraph textModule textModule--type-paragraph hasFeaturedAuthor">Pour le professeur Bastien, cette incohérence remonte à 2022, avant la campagne électorale. </p>

                    
    <p class="paragraph textModule textModule--type-paragraph hasFeaturedAuthor">« Je pense à la conférence de presse à Lévis quand on a annoncé ce que serait le troisième lien, explique-t-il. Déjà, les études n’étaient pas là. On prétextait qu’elles dataient d’avant la pandémie et qu’il fallait les refaire, alors que clairement, l’impact de la pandémie allait être négatif sur le flux de circulation. Le discours du gouvernement était déjà empreint d’incohérence. Puis en avril, quand on a renoncé au projet, il n’y avait pas de nouvel argumentaire. Avec la sortie de cette semaine, cette incohérence m’apparaît toujours comme un problème. » </p>

                    
    <p class="paragraph textModule textModule--type-paragraph hasFeaturedAuthor">« C’est très rare qu’on joue ainsi au yoyo avec un projet, estime pour sa part Éric Bélanger, qui est également membre de la Chaire de recherche sur la démocratie et les institutions parlementaires. Au cours des trois prochaines années, il est fort possible qu’on se pose de plus en plus la question de l’alternative à la CAQ. On est peut-être au début de ce processus. » </p>

                    
    <p class="paragraph textModule textModule--type-paragraph hasFeaturedAuthor">Une analyse que partage Frédérick Bastien. À moyen et long terme, selon lui, cette incohérence pourrait éroder le soutien de la population au gouvernement. </p>

